/**
 * dump data in hex byte output format
 */
void dump_data(const char *s_String, const uint8_t * au8_Data, const uint32_t ou32_Length)
{
   printf("\r\n%s ", s_String);
   for (uint32_t i = 0; i<ou32_Length; i++)
   {
      printf("%02X ", au8_Data[i]);
   }
   printf("\r\n");
}
